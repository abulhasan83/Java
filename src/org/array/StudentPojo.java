package org.array;

public class StudentPojo {
private int id;
private String name;
private long regNo;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Long getRegNo() {
	return regNo;
}
public void setRegNo(Long regNo) {
	this.regNo = regNo;
}


}
